public class ChuDe {
    private String hinhChuDeLon;
    private String hinhChuDeNho;
    private String tenChuDe;
    private int maDanhSach;
    public String getHinhChuDeLon() {
        return hinhChuDeLon;
    }

    public void setHinhChuDeLon(String hinhChuDeLon) {
        this.hinhChuDeLon = hinhChuDeLon;
    }

    public String getHinhChuDeNho() {
        return hinhChuDeNho;
    }

    public void setHinhChuDeNho(String hinhChuDeNho) {
        this.hinhChuDeNho = hinhChuDeNho;
    }

    public String getTenChuDe() {
        return tenChuDe;
    }

    public void setTenChuDe(String tenChuDe) {
        this.tenChuDe = tenChuDe;
    }

    public int getMaDanhSach() {
        return maDanhSach;
    }

    public void setMaDanhSach(int maDanhSach) {
        this.maDanhSach = maDanhSach;
    }
}
